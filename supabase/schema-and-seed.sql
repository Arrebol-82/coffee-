  select *
  from products
  where id = ((o.id + 6) % 28) + 1
) p2 on true;

update orders o
set total_amount = item_totals.total_amount
from (
  select order_id, sum(price * count)::integer as total_amount
  from order_items
  group by order_id
) item_totals
where o.id = item_totals.order_id;

