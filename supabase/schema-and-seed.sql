-- BrewMaster Admin Supabase schema and seed data.
-- Run this in Supabase SQL Editor for a fresh development database.
-- Security note:
-- - RLS is enabled on every table.
-- - The anon write policies below are for this project's temporary development mode only,
--   because the current app still uses mock login instead of Supabase Auth.
-- - Before production, remove anon write policies and replace them with authenticated
--   role-based policies backed by Supabase Auth + profiles.role.
-- - Never put a service_role key or database password in frontend code.

create table if not exists products (
  id bigint generated by default as identity primary key,
  name text not null,
  price integer not null check (price >= 0),
  status text not null check (status in ('on_sale', 'sold_out')),
  stock integer not null default 0 check (stock >= 0),
  category text not null check (category in ('beans', 'espresso', 'drip_bag', 'instant', 'gear')),
  origin text not null default '',
  roast_level text not null check (roast_level in ('light', 'medium', 'dark')),
  description text not null default '',
  recommended boolean not null default false,
  low_stock_threshold integer not null default 12,
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists inventory_logs (
  id bigint generated by default as identity primary key,
  product_id bigint references products(id) on delete set null,
  product_name text not null,
  type text not null check (type in ('in', 'out', 'adjust', 'restore')),
  change integer not null,
  before_stock integer not null,
  after_stock integer not null,
  reason text not null default '',
  operator text not null default 'Admin',
  created_at timestamptz not null default now()
);

create table if not exists orders (
  id bigint generated by default as identity primary key,
  order_no text not null unique,
  customer_name text not null,
  customer_phone text not null,
  customer_address text not null,
  customer_remark text not null default '',
  total_amount integer not null default 0 check (total_amount >= 0),
  status text not null check (status in ('pending', 'paid', 'shipped', 'completed', 'cancelled')),
  created_at timestamptz not null default now()
);

create table if not exists order_items (
  id bigint generated by default as identity primary key,
  order_id bigint not null references orders(id) on delete cascade,
  product_id bigint references products(id) on delete set null,
  product_name text not null,
  price integer not null check (price >= 0),
  count integer not null check (count > 0)
);

create table if not exists order_logs (
  id bigint generated by default as identity primary key,
  order_id bigint not null references orders(id) on delete cascade,
  action text not null,
  operator text not null default 'Admin',
  created_at timestamptz not null default now()
);

create table if not exists sales_channels (
  id bigint generated by default as identity primary key,
  name text not null,
  value integer not null default 0,
  color text not null default '#409eff',
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text,
  role text not null default 'staff' check (role in ('admin', 'staff')),
  permissions text[] not null default '{}',
  created_at timestamptz not null default now()
);

create index if not exists idx_products_active_created_at on products (is_active, created_at desc);
create index if not exists idx_products_status on products (status);
create index if not exists idx_products_category on products (category);
create index if not exists idx_orders_created_at on orders (created_at desc);
create index if not exists idx_orders_status on orders (status);
create index if not exists idx_order_items_order_id on order_items (order_id);
create index if not exists idx_order_logs_order_id on order_logs (order_id);
create index if not exists idx_inventory_logs_created_at on inventory_logs (created_at desc);

