insert into order_items (order_id, product_id, product_name, price, count)
select
  o.id,
  p1.id,
  p1.name,
  p1.price,
  1
from orders o
join lateral (
  select *
  from products
  where id = ((o.id - 1) % 28) + 1
) p1 on true;

insert into order_items (order_id, product_id, product_name, price, count)
select
  o.id,
  p2.id,
  p2.name,
  p2.price,
  2
from orders o
